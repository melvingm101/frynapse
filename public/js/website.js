hashCode = function(s){
  return s.split("").reduce(function(a,b){a=((a<<5)-a)+b.charCodeAt(0);return a&a},0);              
}

function ques1(){
    console.log("This works!");
    var answer = document.getElementById("ques1");
    var answer1 = answer.value + "";
    if (hashCode(answer1.toLowerCase()) == -1033060309){
        window.location.href = "107944096.html";
    }
    
    else{
        alert("The answer is wrong. Try again!!");
    }
}

